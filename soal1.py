# Mengambil input nilai dari pengguna
nilai = input("Masukkan nilai Anda: ")

# Mengonversi nilai yang diinput dari string menjadi tipe float (angka desimal)
nilai = float(nilai)

# Memeriksa kondisi berdasarkan nilai yang dimasukkan
if nilai >= 90:
    print("Excellent performance")  # Jika nilai >= 90, cetak "Excellent performance"
elif nilai >= 80:
    print("Very good performance")  # Jika nilai >= 80, cetak "Very good performance"
elif nilai >= 70:
    print("Good performance")  # Jika nilai >= 70, cetak "Good performance"
elif nilai >= 60:
    print("Average performance")  # Jika nilai >= 60, cetak "Average performance"
else:
    print("Bad performance")  # Jika nilai kurang dari 60, cetak "Bad performance"




